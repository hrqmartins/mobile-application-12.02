/*
O for é usado quando sabemos exatamente quantas vezes o bloco deve ser executado. Ele é composto por três partes: 
inicialização, condição e incremento.
*/

for (let i = 0; i < 5; i++) {
  console.log("Contador:", i);
}
// Saída:
// Contador: 0
// Contador: 1
// Contador: 2
// Contador: 3
// Contador: 4