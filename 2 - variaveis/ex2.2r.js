/**
 * O operador typeof revela o tipo de uma variável em tempo de execução. É útil para depurar e entender como o JavaScript categoriza os dados.
 */

let cidade = "São Paulo";
let populacao = 12300000;
const ativa = true;
console.log(typeof cidade);     // "string"
console.log(typeof populacao);  // "number"
console.log(typeof ativa);      // "boolean"